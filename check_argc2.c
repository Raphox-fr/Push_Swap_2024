#include "push_swap.h"

int	check_args2(char *str)
{
	int i;
	int k;

	k = 0;
	i = 0;

	while (str[i])
	{
		if ((str[i] >= '0' && str[i] <= '9') && str[i] != '\0')
		{
			k++;
		}
		i++;
	}
	if (k >= 1)
		return (1);
	return (0);
}